// Function that returns the sum of two numbers
fun sum(a: Int, b: Int): Int {
    return a + b
}

fun main() {
    // Call the sum function with different values
    val result1 = sum(5, 3)
    val result2 = sum(10, 15)
    val result3 = sum(-2, 7)

    // Display the results
    println("The sum of 5 and 3 is: $result1")
    println("The sum of 10 and 15 is: $result2")
    println("The sum of -2 and 7 is: $result3")
}
